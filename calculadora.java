    public static boolean  ExistePunto(String cadena){
        boolean resultado;
        resultado = false;
        
        for (int i = 0; i< cadena.length(); i++){
            if (cadena.substring(i, i+1).equals (".")){
            resultado = true;
            break;
        }
        }
        return resultado;
    }
    
    public static String Operacion(String n1, String signo, String n2){
        Double total = 0.0;
        String resultado;
        
        if(signo.equals("/")){
            total = Double.parseDouble(n1)/Double.parseDouble(n2);
        }
        if(signo.equals("+")){
            total = Double.parseDouble(n1)+Double.parseDouble(n2);
        }
        if(signo.equals("-")){
            total = Double.parseDouble(n1)-Double.parseDouble(n2);
        }
        if(signo.equals("*")){
            total = Double.parseDouble(n1)*Double.parseDouble(n2);
        }
        
        resultado = total.toString();
        return resultado;
                
    }
